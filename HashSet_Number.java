package learning.collections;
import java.util.*;
public class HashSet_Number {
	public static void main(String[] args)
	{
		HashSet<Integer> numberSet=new HashSet<>();
		
		numberSet.add(100);
		numberSet.add(1000);
		numberSet.add(266);
		numberSet.add(7000);
		numberSet.add(80000);
		System.out.println(numberSet);
	}

}
